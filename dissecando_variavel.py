n = input('Digite algo: ')
print('o tipo primitivo desse valor é:', type(n))
print('\033[1;36m{}\033[m é uma letra?'.format(n), n.isalpha())
print('\033[1;36m{}\033[m é um número?'.format(n), n.isnumeric())
print('\033[1;36m{}\033[m é um número decimal?'.format(n), n.isdecimal())
print('\033[1;36m{}\033[m vai de 0 a 127?'.format(n), n.isascii())
print('\033[1;36m{}\033[m tem números em sua composição?'.format(n), n.isdigit())
print('\033[1;36m{}\033[m é identificado por python?'.format(n), n.isidentifier())
print('\033[1;36m{}\033[m tem letras minúsculas?'.format(n), n.islower())
print('\033[1;36m{}\033[m ocupa espaço em tela ou papel(imprimíve)?'.format(n), n.isprintable())
print('\033[1;36m{}\033[m só tem espaço?'.format(n), n.isspace())
print('\033[1;36m{}\033[m está capitalizada?'.format(n), n.istitle())
print('\033[1;36m{}\033[m tem letras maiúsculas?'.format(n), n.isupper())
